        public static int[][] getPaletteFromFile(string path)
        {
            byte[] bytes = new byte[256 * 3];
            int[][] colorsArray = new int[256][];

            using (BinaryReader reader = new BinaryReader(new FileStream(path, FileMode.Open)))
            {
                reader.Read(bytes, 0, bytes.Length);

                int n = 0;
                for (int i = 0; i < bytes.Length; i += 3)
                {
                    colorsArray[n] = new int[3]
                    {
                        Convert.ToInt16(bytes[i]),
                        Convert.ToInt16(bytes[i+1]),
                        Convert.ToInt16(bytes[i+2])
                    };
                    n++;
                }
            }
            return colorsArray;
        }

        public static Color getPixelColor(int index)
        {
            Color color = Color.FromArgb(colorsArray[index][0], colorsArray[index][1], colorsArray[index][2]);
            return color;
        }

